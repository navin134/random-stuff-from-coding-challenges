public class ForceCalculations {
    private int yForceVector;
    private int xForceVector;
    private int summedAngle;
    private int forceOfairfoils;
    private int forceOfMIP;
    
    public ForceCalculations() {
        this.yForceVector = 0;
        this.xForceVector = 0;
        this.summedAngle = 0;
        this.forceOfairfoils = 0;
        this.forceOfMIP = 0;
        
    }
    
    public void getYForceVector() {
        return this.yForceVector;
        
    }
    
    public void getXForceVector() {
        return this.xForceVector;
        
    }
    
    public void getSummedAngle() {
        return this.summedAngle;
        
    }
    
    public void getForceOfairfoils() {
        return this.forceOfairfoils;
        
    }
    
    public void getForceOfMIP() {
        return this.forceOfMIP;
        
    }
    
    
    
}  
